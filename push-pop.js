// Add elements to an Array. It's called push.
var lastBence = ['Mahmodul', 'Shahin', 'Ashik', 'Naeem', 'Junayed'];
lastBence.push('Rayhan');
lastBence.push('Irfan');
lastBence.push('Alamin');
console.log(lastBence);
// another example
var rollNumbers = [3, 25, 6, 16]
